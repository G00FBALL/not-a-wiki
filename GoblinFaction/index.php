<p><b>FC Formula</b>: floor(ln^3(1 + x) + 20, where x is your current gems.</p>
<p><b>Production Formula</b>: 0.75 * ln^3.15(1 + x)%, where x is your Faction Coins Gained (This Game) stat.</p>

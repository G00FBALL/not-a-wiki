<p>Enter the Research Facilities to improve your grinding potential.</p>
<p>There you will be able to advance your research progress, unlock and buy special upgrades which will boost all your abilities hugely.</p>
<p>Your research points in each branch are limited by the amount of Reincarnations you have.</p>
<p>Research was implemented in the Secrets expansion series (Secrets of the Realm, Secrets of the ancients, Secrets of the underworld) bringing "secret" powers to enhance each mythical faction. Overtaking the Mercenaries, it is accessible from R16 for Vanilla, R23 for Neutral, and R29 for Prestige Factions.</p>
<p><b>----------------------------------</b></p>
<p><b>The Facilities & Related Factions</b>
<p>Each Facility is related to several Factions: Vanilla factions are related to one facility, Neutral to two, and Prestige to three. Each facility increases the production of the related Faction's production according to following formula:
<p><b>RP Production Formula</b>: 350x^0.75% or 0% if x is 0, where x is the number of corresponding Research Points.
<p><b>Facility cost</b>: 10 Notg (1E121) coins, 1M (1E6) Faction coins of the related faction(s), 1 Dvg (1E69) gems
<p><b>Note</b>: It is recommended to start research with 100 Dvg (1E71) - 1 Tvg (1E72) gems.

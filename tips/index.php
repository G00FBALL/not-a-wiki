<p><b>Game's window relationships</b></p>
<p>While your game is not in active tab or window in which the game runs is minimized, all processes slow down so it's generally 
adviced to open a new window for a game, and run it in background but <b>not</b> minimized.</p>
<p><b>Reseting spell timers</b></p>
<p>You can cast spells without waiting for them to run out by exporting the game, and then importing it again.This resets all spels,
which is especially important with spells like Brainwave, and with trophies like Prismatic mana. If you want to automate the process
you can also set your auto-clicker to Cast a spell-&gt;Open settings-&gt;Export-&gt;Import-&gt;Close settings.</p>

<p><b>In-game description</b>
<p>Abdicating will reset all buildings, upgrades and coins, but you will keep your trophies and be awarded with gems that will boost your production. Otherwise, you may also spend Rubies to gain Gems without resetting!
<p>Abdicating is the first kind of soft reset in Realm Grinder. It allows you to cash in all the gems you have earned in order to boost your production. In exchange, you will lose everything except for:
<p>Unlocked Factions
<p>Gained Trophies
<p>Completed Challenges
<p>Discovered Artifacts
<p>Rubies and Ruby Upgrades
<p>"Times Allied with..." stats (all other stats are reset)
<p>Premium Upgrades
<p><b>When to Abdicate?</b>

<p>There is no hard and fast rule as to when you should abdicate on a single run (like multiply gems by x10). Some runs should be huge multipliers, others will be small when you reach one of the many gem walls in the game.
<p>Ask yourself: "Will I be able to increase my income significantly by buying an upgrade after the next couple of spell combos?" If you won’t be able to significantly increase your income soon but you have multiplied your gems to a level where you can get back to where you are easily enough, you should abdicate and make use of the gems you've earned.
<p>To put it simply, if you are using the correct Faction for the gem range you are in, abdicate when you have significantly improved your gems and have no chance of significantly increasing your production within a couple of maximum spell combos. Check that there are no trophies you can easily get on your current run before abdicating.
Tips

Get used to making backups before abdicating so you can always undo if necessary. You can also test abdicate in another game using a private window.

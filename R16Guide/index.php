<p><b>To unlock Research follow the guide below, Do not buy Prestige Faction<p></b>
<p>The original post can be found <a target="_blank" href="http://www.kongregate.com/forums/8945-realm-grinder/topics/562011"><b>here</b></a> by <a target="_blank" href="http://www.kongregate.com/accounts/why_amihere"><b> why_amihere</b></a>
<p>This guide was written at v1.4.39. As of v1.5.42 there is an extra building upgrade tier, which makes the gem requirements both more relaxed and easier to reach. Therefore I would still recommend getting at least close to the lower end of the suggested gem ranges before trying to do things, but stop Mercs when you can’t double your gems in a 1h run.
<p>This guide is to help you make it through the Research phase of R16 reasonably quickly, without wasting time on upgrades that are too hard to get or take a really long time when they would be much faster later, and without getting lost amid the complexity of it all.

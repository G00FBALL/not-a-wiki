        <div class="autohide">
            <p><b>(<a target="_blank" href="http://www.kongregate.com/forums/8945-realm-grinder/topics/605675-secret-trophy-guide-easiest-to-later">Secret Trophy Guide</a></b>) shows in which order it is easier to get the trophies. (Work in progress - 3/32016 by dox4242)</p>
            <p><b>Leet</b>: If you have a premium upgrade, turn it off in the options to start a game with 0 coins.
            <p><b>Equality</b>: In case you have one alignment playtime much longer than an other: restart and do it after Reincarnations. Play times will be reset to zero. When two alignments are one minute apart, the third can be done while offline.</p>
            <p>Building, coin, click, spell, mana, assistants... trophies: use special merc builds. To get all the building trophies, tweak those builds to fit other alignments. These can also be done using research but it won't be easy at R16.</p>
            <p><b>5k Building Type Trophies</b>: With no gifts, tiers 1, 2 and 3 buildings can be done fairely easily below R20 using the Fairy Faction. Unique buildings can be done with individual runs around R25-30. All the others will requier considerable effort and time, and to be R36+ <b>(<a target="_blank" href="http://www.kongregate.com/forums/8945-realm-grinder/topics/605094">Source</a>)</b></p>
            <p><b>No Recruiting</b>: Consider the reward (Increase Mercenary production by 1% per Reincarnation) and decide how much time you are ready to invest into getting this trophy. It can take +3 days at R20, still over 2 days at R25 and less than a day around R28 (using no <a target="_blank" href="http://musicfamily.org/realm/Gifts">Gifts</a>). As for tactic: many players have reported using Fairy-Dwarf as replacement to Merc, and U-Goblin with Angel Bloodline for any gem wall.</p>
            <p><b>That Excavated Quickly</b>: Around R20, make sure to keep your excavations low until you have enough gems to excavate 1000 times - make a save file, abdicate, then try go for the trophy. Undo if necessary.
            <p><b>The Matrix</b>: Careful not to skip 4767 mana, when close to 4k mana buy buildings buy one by one.
            <p><b>5K buildings</b>: <a target="_blank" href="http://www.kongregate.com/forums/8945-realm-grinder/topics/614615-recommendations-for-5k-neutral-building-trophies">source</a>
            <p><b>Suggestion Master</b>, glho kohhl snod, do not interfere with getting Need a Head Start? These can all be done in the first five minutes of a run. (Tested. Tip thanks to this comment)</p></div>

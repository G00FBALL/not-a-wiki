        <center><b>Factions</b></center>
        <p><b>Fairy (3,2)</b>: Simplifies notation of Faction upgrades. The first number represents the tier, and the second the position.</p>
        <p>For combined Faction names, the prestiges' precede and is shortened to whatever sounds best.</p>
        <p><b>Dwangels</b>: Dwarven+Angel</p>
        <p><b>Dwaries</b>: Dwarven+Fairy</p>
        <p><b>Dwelf</b>: Dwarven+Elven</p>
        <p><b>Dromon</b>: Drow+Demon</p>
        <p><b>Droblins</b>: Drow+Goblin</p>
        <p><b>Draguid</b>: Dragon+Druid</p>
        <hr>
        <center><b>Mercenary Template & Faction Abbreviations</b></center>
        <p>Faction abbreviations used in Merc Build templates to identify faction upgrades. These are followed by the numbers 1-12 reflecting their overall position. (eg: Dwarven(3,1) Battlehammers = DN7)</p>
        <p><b>FR</b>: Fairy</p>
        <p><b>EL</b>: Elven</p>
        <p><b>AN</b>: Angel</p>
        <p><b>GB</b>: Goblin</p>
        <p><b>UD</b>: Undead</p>
        <p><b>DM</b>: Demon</p>
        <p><b>TT</b>: Titan</p>
        <p><b>DD</b>: Druid</p>
        <p><b>FC</b>: Faceless</p>
        <p><b>DN</b>: Dwarven</p>
        <p><b>DW</b>: Drow</p>
        <p><b>MC</b>: Mercenary</p>
        <p><b>DG</b>: Dragon</p>
        <p><b>AR</b>: Archon</p>
        <p><b>DJ</b>: Djinn</p>
        <p><b>MK</b>: Makers</p>
        <p><b>UB:Name</b>: Unique Building:UB Name</p>
        <p><b>MA:</b>: Mercenary 13th Upgrade (Good and Evil only)(Not available in A3)</p>
        <p><b>UNN:</b>: UN=Merc Union(Primary), 2nd N=2nd Union(secondary)</p>
		<hr>
		<center><b>Legacies</b></center>
		<p>Legacy abbreviations are based on the Faction following the tier.</p>
		<p><b>B</b>: Bronze</p>
		<p><b>S</b>: Silver</p>
		<p><b>G</b>: Gold</p>
		<p><b>D</b>: Diamond</p>
		<p>Examples: ANB (Angel Bronze Legacy), GBS (Goblin Silver Legacy)</p>
		<hr>
		<center><b>Challenges</b></center>	
		<p>Challenge abbreviations draw off faction ones and add C# at the end.</p>
		<p>Example: DDC4 means Druid Challenge 4.</p>
		<p>CR refers to the spell challenge reward, so TTCR refers to Thunderstorm, the Titan Spell Challenge reward.</p>
